// multiplication.js

// Simple multiplication function
function multiply(num1, num2) {
    return num1 * num2;
}

// Perform multiplications and print the results
console.log(multiply(1, 1)); // Output: 1
console.log(multiply(2, 2)); // Output: 4
console.log(multiply(3, 3)); // Output: 9
console.log(multiply(4, 4)); // Output: 16
console.log(multiply(23, 25)); // Output: 575
console.log(multiply(25, 23)); // Output: 575
